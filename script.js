let num = 50;

switch (num) {
    case num < 49:
        console.log("False!");
        break;
    case num < 100:
        console.log("Too much!");
        break;
    case num > 80:
        console.log("Else too much!");
        break;
    case 50:
        console.log("True!");
        break;
    default:
        console.log("Something wrong!");
        break;
}